export const makeSlug = (title: string) => {
    const replacements: Record<string, string> = {
        'á': 'a', 'à': 'a', 'ä': 'a', 'â': 'a', 'ā': 'a', 'ă': 'a',
        'é': 'e', 'è': 'e', 'ë': 'e', 'ê': 'e', 'ē': 'e', 'ĕ': 'e',
        'í': 'i', 'ì': 'i', 'ï': 'i', 'î': 'i', 'ī': 'i', 'ĭ': 'i',
        'ó': 'o', 'ò': 'o', 'ö': 'o', 'ô': 'o', 'ō': 'o', 'ŏ': 'o',
        'ú': 'u', 'ù': 'u', 'ü': 'u', 'û': 'u', 'ū': 'u', 'ŭ': 'u',
        'ñ': 'n', 'ç': 'c',
        // Заглавные тоже
        'Á': 'A', 'À': 'A', 'Ä': 'A', 'Â': 'A', 'Ā': 'A', 'Ă': 'A',
        'É': 'E', 'È': 'E', 'Ë': 'E', 'Ê': 'E', 'Ē': 'E', 'Ĕ': 'E',
        'Í': 'I', 'Ì': 'I', 'Ï': 'I', 'Î': 'I', 'Ī': 'I', 'Ĭ': 'I',
        'Ó': 'O', 'Ò': 'O', 'Ö': 'O', 'Ô': 'O', 'Ō': 'O', 'Ŏ': 'O',
        'Ú': 'U', 'Ù': 'U', 'Ü': 'U', 'Û': 'U', 'Ū': 'U', 'Ŭ': 'U',
        'Ñ': 'N', 'Ç': 'C'
    };

    return title
        .toLowerCase()
        // Заменяем специальные символы
        .replace(/[áàäâāăéèëêēĕíìïîīĭóòöôōŏúùüûūŭñç]/g, (match) => replacements[match] || match)
        // Убираем все кроме букв, цифр, пробелов и дефисов
        .replace(/[^a-z0-9\s-]/g, "")
        // Заменяем пробелы на дефисы
        .replace(/\s+/g, "-")
        .trim();
};